import type { GenerationOutput } from "@/types";
import { BaseBuildAgent, type GeneratedFile } from "./base-build.agent";

export class DocsBuildAgent extends BaseBuildAgent {
  name = "DocsBuildAgent";
  label = "Handover documents";

  async run(output: GenerationOutput): Promise<GeneratedFile[]> {
    const [readme, deployment, database] = await Promise.all([
      this.generate(
        `You are a technical writer. Generate a complete, professional README.md for a SaaS application.
Return ONLY raw Markdown — no code fences wrapping the entire output.
Include these sections:
# [App Name]
## Overview (2-3 sentences)
## Features (bulleted list)
## Tech Stack (table)
## Quick Start (numbered steps: clone, install, env vars, db setup, run)
## Environment Variables (table with name, description, where to get it)
## Deployment (brief Vercel guide)
## Architecture (brief description of folder structure)`,
        `App context:\n${this.context(output)}\n\nFull blueprint:\n${JSON.stringify({ overview: output.overview, pages: output.pages, api_routes: output.api_routes }, null, 2)}`
      ),
      this.generate(
        `You are a DevOps expert. Generate a complete DEPLOYMENT.md guide for deploying this SaaS to production.
Return ONLY raw Markdown.
Include:
## Prerequisites
## Step 1: Supabase Setup (create project, run migration, configure auth, get keys)
## Step 2: Stripe Setup (create products with exact pricing, get price IDs, configure webhook)
## Step 3: Vercel Deployment (connect GitHub, set all env vars with descriptions, deploy)
## Step 4: Post-Deployment Checklist (test auth, test payment, verify webhook)
## Troubleshooting (common issues and fixes)`,
        `App context:\n${this.context(output)}\n\nStripe plans:\n${JSON.stringify(output.stripe_plans, null, 2)}`
      ),
      this.generate(
        `You are a database engineer. Generate a complete DATABASE.md guide.
Return ONLY raw Markdown.
Include:
## Schema Overview
## Tables (each table with column descriptions and purpose)
## Row Level Security (explain each RLS policy)
## Running Migrations (exact commands)
## Seeding Data (example seed data for testing)
## Backups (Supabase backup configuration)`,
        `App context:\n${this.context(output)}\n\nDatabase schema:\n${JSON.stringify(output.database_schema, null, 2)}`
      ),
    ]);

    const handover = `# ${output.overview.name} — Handover Document

## What Was Built
${output.overview.description}

## What's Included
- Complete Next.js 15 application with TypeScript
- Supabase authentication (email + Google OAuth)
- Prisma ORM with PostgreSQL (Supabase)
- Stripe billing with subscription management
- All pages: ${output.pages.map(p => p.name).join(", ")}
- All API routes: ${output.api_routes.map(r => `${r.method} ${r.path}`).join(", ")}

## Immediate Next Steps
1. Follow DEPLOYMENT.md to get the app live (est. 30-45 min)
2. Run the SQL migration in Supabase SQL Editor
3. Create Stripe products matching the pricing plans
4. Configure Google OAuth in Supabase

## Credentials Needed
- Supabase project (supabase.com)
- Stripe account (stripe.com)
- Vercel account (vercel.com)
- Anthropic API key (console.anthropic.com)

## Architecture Decisions
- **Auth**: Supabase handles all authentication — no custom auth code to maintain
- **Database**: Prisma ORM gives you type-safe queries; Supabase hosts the Postgres DB
- **Billing**: Stripe handles all payment processing; webhooks sync subscription state
- **Deployment**: Vercel auto-deploys on every git push to main

## Support
Generated by LaunchForge AI. For issues, check the troubleshooting section in DEPLOYMENT.md.
`;

    return [
      { path: "README.md", content: readme },
      { path: "DEPLOYMENT.md", content: deployment },
      { path: "DATABASE.md", content: database },
      { path: "HANDOVER.md", content: handover },
    ];
  }
}
